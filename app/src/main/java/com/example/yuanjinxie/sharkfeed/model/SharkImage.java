package com.example.yuanjinxie.sharkfeed.model;

public class SharkImage {
    private String Title;
    private String Content;
    private String FlickerLink;
    private String ID;


    public void setTitle(String Title) {
        this.Title = Title;
    }

    public String getTitle() {
        return Title;
    }

    public void setContent(String Content) {
        this.Content = Content;
    }

    public String getContent() {
        return Content;
    }

    public void setFlickerLink(String FlickerLink) {
        this.FlickerLink = FlickerLink;
    }

    public String getFlickerLink() {
        return FlickerLink;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getID() {
        return ID;
    }
}
